package cn.gov.nlc.vo;

public class OsDetail {

	private String baseos;
	
	private Integer newUserNum;
	
	private String newUserRatio;
	
	private Integer newStartNum;
	
	private String newStartRatio;

	public String getBaseos() {
		return baseos;
	}

	public void setBaseos(String baseos) {
		this.baseos = baseos;
	}

	public Integer getNewUserNum() {
		return newUserNum;
	}

	public void setNewUserNum(Integer newUserNum) {
		this.newUserNum = newUserNum;
	}

	public String getNewUserRatio() {
		return newUserRatio;
	}

	public void setNewUserRatio(String newUserRatio) {
		this.newUserRatio = newUserRatio;
	}

	public Integer getNewStartNum() {
		return newStartNum;
	}

	public void setNewStartNum(Integer newStartNum) {
		this.newStartNum = newStartNum;
	}

	public String getNewStartRatio() {
		return newStartRatio;
	}

	public void setNewStartRatio(String newStartRatio) {
		this.newStartRatio = newStartRatio;
	}
	
	
}
