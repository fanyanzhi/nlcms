package cn.gov.nlc.vo;
//各资源画像 阅读详情
public class Ydqk {
	private String zyID;	//资源ID
	private String zymc;	//资源名称
	private int  ydcs;		//阅读次数
	private float ydsc;	//阅读时长
	public String getZyID() {
		return zyID;
	}
	public void setZyID(String zyID) {
		this.zyID = zyID;
	}
	public String getZymc() {
		return zymc;
	}
	public void setZymc(String zymc) {
		this.zymc = zymc;
	}
	public int getYdcs() {
		return ydcs;
	}
	public void setYdcs(int ydcs) {
		this.ydcs = ydcs;
	}
	public float getYdsc() {
		return ydsc;
	}
	public void setYdsc(float ydsc) {
		this.ydsc = ydsc;
	}
	
}
